const { body, param, query } = require("express-validator"); // Импорт методов валидации из express-validator
const config = require("./config"); // Импорт конфигурации приложения

const createOrder = [
  // Правила валидации для создания нового заказа
  body("customerId") // Валидация поля "customerId"
    .isInt({ min: 1 }) // Проверяет, что ID клиента - целое число >= 1
    .withMessage("customerId должен быть положительным целым числом"), // Сообщение при неверном формате

  body("pvzId") // Валидация поля "pvzId"
    .isInt({ min: 1 }) // Исправлено: isInt вместо isIn - проверка целого числа >= 1
    .withMessage("pvzId должен быть положительным целым числом"), // Сообщение при неверном формате

  body("status") // Валидация поля "status" (опционально)
    .optional() // Делает поле опциональным
    .isIn(config.ORDER_STATUSES) // Проверяет, что статус из разрешенного списка
    .withMessage(
      // Сообщение при неверном статусе
      `Статус должен быть одним из: ${config.ORDER_STATUSES.join(", ")}`
    ),

  body("items") // Валидация поля "items"
    .isArray({ min: 1 }) // Проверяет, что items - массив минимум с 1 элементом
    .withMessage("items должен быть непустым массивом"), // Сообщение при неверном формате

  body("items.*.productName") // Валидация productName для каждого элемента массива
    .trim() // Удаляет пробелы в начале и конце
    .notEmpty() // Проверяет, что поле не пустое
    .withMessage("Название товара обязательно"), // Сообщение для пустого поля

  body("items.*.quantity") // Валидация quantity для каждого элемента массива
    .isInt({ min: 1 }) // Исправлено: isInt вместо isIns - проверка целого числа >= 1
    .withMessage("Количество должно быть >= 1"), // Сообщение при неверном количестве (исправлен текст)

  body("items.*.price") // Валидация price для каждого элемента массива
    .isFloat({ min: 0 }) // Проверяет, что цена - число с плавающей точкой >= 0
    .withMessage("Цена должна быть >= 0"), // Сообщение при неверной цене (исправлен текст)
];

const updateOrder = [
  // Правила валидации для обновления существующего заказа
  body("status") // Валидация поля "status" (опционально)
    .optional() // Делает поле опциональным для частичного обновления
    .isIn(config.ORDER_STATUSES) // Проверяет, что статус из разрешенного списка
    .withMessage(
      // Сообщение при неверном статусе
      `Статус должен быть одним из: ${config.ORDER_STATUSES.join(", ")}`
    ),

  body("pvzId") // Валидация поля "pvzId" (опционально)
    .optional() // Делает поле опциональным для частичного обновления
    .isInt({ min: 1 }) // Проверяет, что ID ПВЗ - целое число >= 1
    .withMessage("pvzId должен быть положительным целым числом"), // Сообщение при неверном формате

  param("id") // Валидация параметра маршрута "id"
    .isInt({ min: 1 }) // Проверяет, что ID заказа - целое число >= 1
    .withMessage("ID заказа должен быть положительным целым числом"), // Сообщение при неверном формате
];

const getAllOrdersQuery = [
  // Правила валидации для параметров запроса (пагинация и фильтры)
  query("page") // Валидация query-параметра "page"
    .optional() // Делает параметр опциональным
    .isInt({ min: 1 }) // Проверяет, что номер страницы - целое число >= 1
    .withMessage("page должен быть >= 1"), // Сообщение при неверном значении (исправлен текст)

  query("limit") // Валидация query-параметра "limit"
    .optional() // Делает параметр опциональным
    .isInt({ min: 1, max: 20 }) // Проверяет, что лимит от 1 до 20 включительно
    .withMessage("limit должен быть от 1 до 20"), // Сообщение при неверном значении

  query("status") // Валидация query-параметра "status" (фильтр по статусу)
    .optional() // Делает параметр опциональным
    .isIn(config.ORDER_STATUSES) // Проверяет, что статус из разрешенного списка
    .withMessage(
      // Сообщение при неверном статусе
      `Статус должен быть одним из: ${config.ORDER_STATUSES.join(", ")}`
    ),

  query("pvzId") // Валидация query-параметра "pvzId" (фильтр по ПВЗ)
    .optional() // Делает параметр опциональным
    .isInt({ min: 1 }) // Проверяет, что ID ПВЗ - целое число >= 1
    .withMessage("pvzId должен быть положительным целым числом"), // Сообщение при неверном формате
];

module.exports = {
  // Экспорт правил валидации для использования в других модулях
  createOrder, // Экспорт правил для создания заказа
  updateOrder, // Экспорт правил для обновления заказа
  getAllOrdersQuery, // Экспорт правил для параметров запроса
};
