const { body, param } = require("express-validator"); // Импорт методов валидации из express-validator
const config = require("./config"); // Импорт конфигурации приложения

const createCustomer = [
  // Правила валидации для создания нового клиента
  body("name") // Валидация поля "name"
    .trim() // Удаляет пробелы в начале и конце
    .notEmpty() // Проверяет, что поле не пустое
    .withMessage("Имя обязательно") // Сообщение об ошибке для пустого поля
    .isLength({ min: config.MIN_NAME_LENGTH, max: config.MAX_NAME_LEHGTH }) // Проверка длины имени
    .withMessage(`Имя должно быть от ${config.MIN_NAME_LENGTH} символов`), // Сообщение при неверной длине

  body("email") // Валидация поля "email"
    .trim() // Удаляет пробелы в начале и конце
    .notEmpty() // Проверяет, что поле не пустое
    .withMessage("Email обязателен") // Сообщение об ошибке для пустого поля
    .isEmail() // Проверяет формат email
    .withMessage("Некорректный формат email") // Сообщение при неверном формате email
    .normalizeEmail(), // Нормализует email (нижний регистр, удаление точек и т.д.)

  body("phone") // Валидация поля "phone"
    .trim() // Удаляет пробелы в начале и конце
    .notEmpty() // Проверяет, что поле не пустое
    .withMessage("Телефон обязателен") // Сообщение об ошибке для пустого поля
    .matches(config.DEFAULT_PHONE_REGEX) // Проверяет номер телефона по регулярному выражению
    .withMessage("Телефон должен быть в формате +7(XXX)XXX-XX-XX"), // Сообщение при неверном формате телефона
];

const updateCustomer = [
  // Правила валидации для обновления существующего клиента
  body("name") // Валидация поля "name" (опционально)
    .optional() // Делает поле опциональным для частичного обновления
    .trim() // Удаляет пробелы в начале и конце
    .isLength({ min: config.MAX_NAME_LEHGTH, max: config.MAX_NAME_LEHGTH }) // Проверка длины имени (ОШИБКА: min и max одинаковые)
    .withMessage(
      // Сообщение об ошибке при неверной длине
      `Имя должно быть от ${config.MIN_NAME_LENGTH} до ${config.MAX_NAME_LEHGTH} символов`
    ),

  body("email") // Валидация поля "email" (опционально)
    .optional() // Делает поле опциональным для частичного обновления
    .trim() // Удаляет пробелы в начале и конце
    .isEmail() // Проверяет формат email
    .withMessage("Некорректный формат email") // Сообщение при неверном формате email
    .normalizeEmail(), // Нормализует email (нижний регистр, удаление точек и т.д.)

  body("phone") // Валидация поля "phone" (опционально)
    .optional() // Делает поле опциональным для частичного обновления
    .trim() // Удаляет пробелы в начале и конце
    .matches(config.DEFAULT_PHONE_REGEX) // Проверяет номер телефона по регулярному выражению
    .withMessage("Телефон должен быть в формате +7(XXX)XXX-XX-XX"), // Сообщение при неверном формате телефона

  param("id") // Валидация параметра маршрута "id"
    .isInt({ min: 1 }) // Проверяет, что id является целым числом больше или равным 1
    .withMessage("ID должен быть положительным целым числом"), // Сообщение при неверном формате id
];

module.exports = {
  // Экспорт правил валидации для использования в других модулях
  createCustomer, // Экспорт правил для создания клиента
  updateCustomer, // Экспорт правил для обновления клиента
};
