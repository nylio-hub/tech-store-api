const swaggerJsdoc = require("swagger-jsdoc"); // Импорт библиотеки для генерации Swagger спецификации из JSDoc комментариев
const path = require("path"); // Импорт модуля path для работы с путями файловой системы

const options = {
  // Конфигурация для генерации Swagger документации
  definition: {
    // Определение OpenAPI спецификации
    openapi: "3.0.0", // Версия спецификации OpenAPI
    info: {
      // Информация об API
      title: "Магазин техники API", // Название API
      version: "1.0.0", // Версия API
      // Описание API
      description:
        "REST API для управления клиентами, заказами и пунктами выдачи (ПВЗ)",
    },
    servers: [
      // Список серверов, где доступно API
      {
        // Описание сервера разработки
        url: "http://localhost:3000", // URL локального сервера
        description: "Локальный сервер (разработка)", // Описание окружения
      },
    ],
    components: {
      // Компоненты, используемые в спецификации
      securitySchemes: {
        // Схемы безопасности для авторизации
        basicAuth: {
          // Базовая HTTP аутентификация
          type: "http", // Тип аутентификации - HTTP
          scheme: "basic", // Схема аутентификации - Basic
          // Описание схемы авторизации
          description:
            "Basic Authentication для доступа к административным эндпоинтам. Используйте учетные данные из конфигурации",
        },
      },
    },
  },
  apis: ["./docs/*.yaml"], // Пути к файлам с аннотациями OpenAPI (YAML формат)
};

const specs = swaggerJsdoc(options); // Генерация Swagger спецификации на основе конфигурации

module.exports = specs; // Экспорт сгенерированной спецификации для использования в приложении
